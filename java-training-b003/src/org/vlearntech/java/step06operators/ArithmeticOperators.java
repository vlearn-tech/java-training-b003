package org.vlearntech.java.step06operators;

public class ArithmeticOperators {
	public static void main(String[] args) {
		int a = 23;
		int b = 10;

		System.out.println("a + b = " + (a + b));
		System.out.println("a - b = " + (a - b));
		System.out.println("a * b = " + (a * b));
		System.out.println("a / b = " + ((float) a / b));
		System.out.println("a % b = " + (a % b));

	}
}
